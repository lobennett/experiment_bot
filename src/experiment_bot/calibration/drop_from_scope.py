"""Drop-from-scope machinery for SP11 Phase 5b.

Per user note 3: paradigms that fail the pilot-time alignment after 2
retries should be marked ``task_specific.sp11_supported: false`` in
the TaskCard, skipped by Phase 7's measurement runs, and auto-noted
in the structured unsupported-paradigms file. Failure mode is loud
and structural, not silent.

Surface area:
- :func:`pilot_with_retry` — run a pilot session up to N times; on
  persistent failure, return a verdict capturing the failure reason.
- :func:`mark_taskcard_unsupported` — write the sp11_supported=False
  marker into a TaskCard JSON file alongside an
  ``sp11_unsupported_reason`` string.
- :func:`append_unsupported_note` — append a structured paragraph to
  ``docs/sp11-unsupported.md`` (creating the file if absent).

The CLI's session-start guard (``experiment_bot.cli._run_task``) reads
the sp11_supported flag and refuses to run unsupported paradigms,
referring the user to this module's output.
"""
from __future__ import annotations

import json
import logging
import time
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Awaitable, Callable

logger = logging.getLogger(__name__)


# Path constants — defaults relative to repo root. Callers can override
# via function arguments.
DEFAULT_UNSUPPORTED_DOC = Path("docs/sp11-unsupported.md")


@dataclass
class PilotAttempt:
    attempt: int
    succeeded: bool
    failure_reason: str | None = None
    duration_s: float = 0.0


@dataclass
class PilotVerdict:
    """Outcome of a `pilot_with_retry` invocation."""
    label: str
    supported: bool
    attempts: list[PilotAttempt] = field(default_factory=list)
    final_failure_reason: str | None = None

    @property
    def n_attempts(self) -> int:
        return len(self.attempts)


async def pilot_with_retry(
    label: str,
    pilot_callable: Callable[[], Awaitable[Any]],
    *,
    max_retries: int = 2,
) -> PilotVerdict:
    """Run ``pilot_callable`` up to ``max_retries + 1`` times. Returns
    a :class:`PilotVerdict` capturing each attempt and the overall
    supported flag.

    ``pilot_callable`` should raise on failure and return on success.
    Each retry is a fresh invocation; the caller is responsible for
    constructing the callable (e.g., a closure around an executor +
    URL).

    Per user note 3, ``max_retries=2`` means 1 initial attempt + 2
    retries = 3 total attempts before marking unsupported.
    """
    verdict = PilotVerdict(label=label, supported=False)
    for attempt_idx in range(max_retries + 1):
        attempt = PilotAttempt(attempt=attempt_idx + 1, succeeded=False)
        t_start = time.monotonic()
        try:
            await pilot_callable()
            attempt.succeeded = True
            attempt.duration_s = time.monotonic() - t_start
            verdict.attempts.append(attempt)
            verdict.supported = True
            return verdict
        except Exception as e:
            attempt.duration_s = time.monotonic() - t_start
            attempt.failure_reason = f"{type(e).__name__}: {e}"
            verdict.attempts.append(attempt)
            logger.warning(
                f"Pilot attempt {attempt.attempt}/{max_retries + 1} for "
                f"{label} failed: {attempt.failure_reason}"
            )
    verdict.final_failure_reason = verdict.attempts[-1].failure_reason
    return verdict


def mark_taskcard_unsupported(
    taskcard_path: Path,
    reason: str,
) -> None:
    """Set ``task_specific.sp11_supported = False`` on the TaskCard
    at ``taskcard_path`` and stamp the reason string.

    Modifies the JSON in place. Caller is responsible for committing
    the change to version control.
    """
    data = json.loads(taskcard_path.read_text())
    ts = data.setdefault("task_specific", {})
    ts["sp11_supported"] = False
    ts["sp11_unsupported_reason"] = reason
    taskcard_path.write_text(json.dumps(data, indent=2) + "\n")


def append_unsupported_note(
    label: str,
    reason: str,
    *,
    doc_path: Path = DEFAULT_UNSUPPORTED_DOC,
    n_attempts: int = 0,
    iso_timestamp: str | None = None,
) -> None:
    """Append a structured paragraph to ``docs/sp11-unsupported.md``
    documenting the dropped paradigm. Creates the file with a header
    if it doesn't exist.
    """
    if iso_timestamp is None:
        iso_timestamp = time.strftime("%Y-%m-%dT%H:%M:%S%z", time.localtime())
    doc_path = Path(doc_path)
    doc_path.parent.mkdir(parents=True, exist_ok=True)
    header = (
        "# SP11 unsupported paradigms\n\n"
        "Auto-generated by `experiment_bot.calibration.drop_from_scope.\n"
        "append_unsupported_note`. Each entry corresponds to a paradigm\n"
        "that failed the pilot-time alignment check during Phase 5b\n"
        "regeneration after 2 retries (3 total attempts). These\n"
        "paradigms are not measured in Phase 7 and are not part of\n"
        "the SP11 cross-deployment claim.\n\n"
    )
    if not doc_path.exists():
        doc_path.write_text(header)
    entry = (
        f"## {label}\n\n"
        f"- **Dropped at:** {iso_timestamp}\n"
        f"- **Attempts:** {n_attempts}\n"
        f"- **Reason:** {reason}\n\n"
    )
    with doc_path.open("a") as f:
        f.write(entry)
